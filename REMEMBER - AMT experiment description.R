# https://app.cloudresearch.com/LaunchedSurvey/EditPanelStudyToolkit/121438

Medical brochures cognitive experiment


You will be presented with consent forms and information about medical conditions. Your task will be to perform estimations, make decisions, and to tell us your opinion, feelings, etc.



This is a cognitive science experiment about how we understand consent forms for medical tests and make decisions based on their information. 

If you participate, we need you to take the experiment seriously. The duration of the experiment is expected to be >1 hour. Please, make sure you won't be distracted while completing it.

The experiment is intended ONLY FOR fluent English speakers. 

Please read the questions carefully and try to answer to the best of your ability.

Thanks for your participation!